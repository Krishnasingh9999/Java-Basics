public class DivisionOfDataType {
  public static void main(String[] args) {
    double x = 5/2;
    System.out.println(x); // 2.0

    double y = 5.0/2;
    System.out.println(y); // 2.5

    double z = 5/2.0;
    System.out.println(z); // 2.5

    double a = 5.0/2.0;
    System.out.println(a); // 2.5

    //So if anyone value is in decimal(double) then gives the result in decimal(double)
  }
}
